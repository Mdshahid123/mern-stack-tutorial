//Assignment Operators(=):An assignment operator assigns a value to its left operand based on the value of its right operand

// cpu Move data between memory and registers
a=5;
console.log(a)

//compound assignment operator:assignment operator combined with arithmetic operator
console.log(a+=2)//same as a=a+2 
console.log(a-=2)//same as a=a-2
console.log(a*=3)//same as a=a*2
console.log(a/=2)//same as a=a/2
console.log(a%=2)//same as a=a%2
console.log(a**=2)//same as a=a**2


